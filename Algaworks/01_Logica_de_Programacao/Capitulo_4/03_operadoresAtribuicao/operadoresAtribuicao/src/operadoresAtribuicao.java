public class operadoresAtribuicao {
    public static void main(String[] args){
        double numero = 7;
        System.out.println(numero);

        numero = numero + 4;
        System.out.println(numero);

        numero += 4;
        System.out.println(numero);

        numero -= 8;
        System.out.println(numero);

        numero *= 5;
        System.out.println(numero);

        numero /= 7;
        System.out.println(numero);
    }
}
